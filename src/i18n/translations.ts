export type UiKeys = keyof typeof ui.en;

export function getLangFromUrl(url: URL) {
    // With base: '/portfolio', URLs are: /portfolio/ (en), /portfolio/fr/ (fr), /portfolio/nl/ (nl)
    const segments = url.pathname.split('/').filter(Boolean);
    // segments for /portfolio/ => ['portfolio']
    // segments for /portfolio/fr/ => ['portfolio', 'fr']
    if (segments.length >= 2) {
        const possibleLang = segments[1];
        if (possibleLang in ui) return possibleLang as keyof typeof ui;
    }
    return defaultLang;
}

export function useTranslations(lang: keyof typeof ui) {
    return function t(key: UiKeys) {
        return ui[lang][key] || ui[defaultLang][key];
    };
}

export function getLocalizedPath(lang: keyof typeof ui, path: string) {
    // path already includes /portfolio prefix
    if (lang === defaultLang) return path;
    // Insert lang after /portfolio: /portfolio/#projects => /portfolio/fr/#projects
    return path.replace('/portfolio/', `/portfolio/${lang}/`);
}
